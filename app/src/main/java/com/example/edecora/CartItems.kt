package com.example.edecora

class CartItems {


    var itemId: String? = null
    var itemName: String? = null
    var itemPrice: String? = null
    var iID: String? = null

    // empty constructor
    constructor() {}

    constructor(id: String, itemName: String, itemPrice: String) {
        this.itemId = id
        this.itemName = itemName
        this.itemPrice = itemPrice
        this.iID = iID
    }

}